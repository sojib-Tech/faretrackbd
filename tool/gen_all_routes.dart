String _stopToDart(_Stop s) {
  return "    BusStop(name: '${s.name}', distanceFromStartKm: ${s.dist}),";
}

String _stopToPy(_Stop s) {
  return '            {"name": "${s.name}", "distance_from_start_km": ${s.dist}}';
}

String _fareRowToDart(List<double> row) {
  return '    [${row.map((f) => '${f.toStringAsFixed(1)}').join(', ')}],';
}

String _fareRowToPy(List<double> row) {
  return '            [${row.map((f) => '${f.toStringAsFixed(1)}').join(', ')}]';
}

String _fareMatrixRowToPy(List<double> row, int startFrom) {
  return '            [${row.map((f) => '${f.toStringAsFixed(1)}').join(', ')}]';
}

List<List<double>> _buildFareMatrix(List<_Stop> stops, List<List<double>> fareData) {
  // In the Python version, fare_matrix[i] has n-1-i entries:
  // [fare from i to i+1, fare from i to i+2, ..., fare from i to n-1]
  // Total rows = n-1 (last stop has no subsequent stops)
  final n = stops.length;
  final matrix = <List<double>>[];
  for (int i = 0; i < n - 1; i++) {
    final row = <double>[];
    for (int j = i + 1; j < n; j++) {
      final fare = fareData[j][i]; // fareData[j][i] = fare from j to i where i < j
      row.add(fare);
    }
    matrix.add(row);
  }
  return matrix;
}

List<List<double>> _buildFareMatrixFromFareData(
    int n, List<List<double>> fareData) {
  final matrix = <List<double>>[];
  for (int i = 0; i < n - 1; i++) {
    final row = <double>[];
    for (int j = i + 1; j < n; j++) {
      final fare = fareData[j][i];
      row.add(fare);
    }
    matrix.add(row);
  }
  return matrix;
}

String _generateDartRoute(_NewRoute r, int index) {
  final fareData = _buildFareData(r.stops, r.explicitFares);
  final buf = StringBuffer();
  buf.writeln();
  buf.writeln('// Route ${index + 29}: ${r.nameBn} [${r.id}]');
  buf.writeln('const _route${index + 29} = BusRoute(');
  buf.writeln("  id: '${r.id}',");
  buf.writeln("  nameBn: '${r.nameBn}',");
  buf.writeln("  nameEn: '${r.nameEn}',");
  buf.writeln("  routeNo: '${r.routeNo}',");
  buf.writeln('  totalDistanceKm: ${r.totalKm},');
  buf.writeln('  stops: [');
  for (final s in r.stops) {
    buf.writeln(_stopToDart(s));
  }
  buf.writeln('  ],');
  buf.writeln('  fareData: [');
  for (final row in fareData) {
    buf.writeln(_fareRowToDart(row));
  }
  buf.writeln('  ],');
  buf.writeln(');');
  return buf.toString();
}

String _generatePyRoute(_NewRoute r, int index) {
  final fareData = _buildFareData(r.stops, r.explicitFares);
  final fareMatrix = _buildFareMatrix(r.stops, fareData);
  final buf = StringBuffer();
  buf.writeln(',');
  buf.writeln('    {');
  buf.writeln('        "id": "${r.id}",');
  buf.writeln('        "name_bn": "${r.nameBn}",');
  buf.writeln('        "name_en": "${r.nameEn}",');
  buf.writeln('        "route_no": "${r.routeNo}",');
  buf.writeln('        "total_distance_km": ${r.totalKm},');
  buf.writeln('        "stops": [');
  for (final s in r.stops) {
    buf.writeln(_stopToPy(s) + ',');
  }
  buf.writeln('        ],');
  buf.writeln('        "fare_data": [');
  for (final row in fareData) {
    if (row.isEmpty) {
      buf.writeln('            [],');
    } else {
      buf.writeln(_fareRowToPy(row) + ',');
    }
  }
  buf.writeln('        ],');
  buf.writeln('        "fare_matrix": [');
  for (final row in fareMatrix) {
    buf.writeln(_fareMatrixRowToPy(row, 0) + ',');
  }
  buf.writeln('        ]');
  buf.writeln('    }');
  return buf.toString();
}


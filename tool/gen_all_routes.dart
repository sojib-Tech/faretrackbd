class _MatrixRoute {
  final String id;
  final String nameBn;
  final String nameEn;
  final String routeNo;
  final double totalKm;
  final List<String> stopNames;
  final List<double> km;
  final List<List<int>> fares;
  const _MatrixRoute(this.id, this.nameBn, this.nameEn, this.routeNo,
      this.totalKm, this.stopNames, this.km, this.fares);

  List<List<double>> buildFareData() {
    final n = stopNames.length;
    final fd = List.generate(n, (_) => <double>[]);
    for (int i = 0; i < n; i++) {
      for (int j = 0; j < i; j++) {
        final v = fares[i][j];
        fd[i].add(v == 0 && i != j ? kMinFare : v.toDouble());
      }
    }
    return fd;
  }
}

double _computeFare(double dist) {
  return (dist * kFarePerKm).round().clamp(kMinFare.toInt(), 999).toDouble();
}

List<List<double>> _buildFareData(
    List<_Stop> stops, List<_ExplicitFare> explicitFares) {
  final n = stops.length;
  final fareData = List.generate(n, (_) => <double>[]);

  // Build a lookup for explicit fares
  final explicit = <String, double>{};
  for (final ef in explicitFares) {
    final key = '${ef.from},${ef.to}';
    final revKey = '${ef.to},${ef.from}';
    explicit[key] = ef.fare;
    explicit[revKey] = ef.fare;
  }

  for (int i = 0; i < n; i++) {
    for (int j = 0; j < i; j++) {
      final key = '$i,$j';
      double fare;
      if (explicit.containsKey(key)) {
        fare = explicit[key]!;
      } else {
        final dist = (stops[i].dist - stops[j].dist).abs();
        fare = _computeFare(dist);
      }
      fareData[i].add(fare);
    }
  }
  return fareData;
}


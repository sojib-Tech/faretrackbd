void _generatePythonFile() {
  String content = File(pyOutputPath).readAsStringSync();
  bool has55Py = (content.split('"id":').length - 1) >= 55;
  bool has84Py = (content.split('"id":').length - 1) >= 84;

  if (has84Py) {
    print('Python file already has 84 routes. Skipping.');
    return;
  }

  // Phase 1: Add 27 routes if needed
  if (!has55Py) {
    final pyTrimmed = content.trimRight();
    if (!pyTrimmed.endsWith(']')) {
      throw Exception('Expected Python file to end with ]');
    }
    final pyBefore = pyTrimmed.substring(0, pyTrimmed.length - 1).trimRight();
    final newRoutesPy = StringBuffer();
    for (int i = 0; i < kNewRoutes.length; i++) {
      newRoutesPy.write(_generatePyRoute(kNewRoutes[i], i));
    }
    content = '$pyBefore\n${newRoutesPy}\n]\n';
    File(pyOutputPath).writeAsStringSync(content);
    print('Python phase 1 done: added 27 routes.');
  }

  // Phase 2: Add 29 routes if needed
  content = File(pyOutputPath).readAsStringSync();
  bool nowHas84 = (content.split('"id":').length - 1) >= 84;
  if (!nowHas84) {
    final pyTrimmed = content.trimRight();
    if (!pyTrimmed.endsWith(']')) {
      throw Exception('Expected Python file to end with ]');
    }
    final pyBefore = pyTrimmed.substring(0, pyTrimmed.length - 1).trimRight();
    final newRoutesPy = StringBuffer();
    for (int i = 0; i < kMatrixRoutes.length; i++) {
      newRoutesPy.write(_generateMatrixPyRoute(kMatrixRoutes[i], i));
    }
    final fullPy = '$pyBefore\n${newRoutesPy}\n]\n';
    File(pyOutputPath).writeAsStringSync(fullPy);
    print('Python phase 2 done: added 29 routes.');
  }
}

  buf.writeln('// Route $routeIndex: ${r.nameBn} [${r.id}]');
  buf.writeln('const _route$routeIndex = BusRoute(');
  buf.writeln("  id: '${r.id}',");
  buf.writeln("  nameBn: '${r.nameBn}',");
  buf.writeln("  nameEn: '${r.nameEn}',");
  buf.writeln("  routeNo: '${r.routeNo}',");
  buf.writeln('  totalDistanceKm: ${r.totalKm},');
  buf.writeln('  stops: [');
  for (int i = 0; i < r.stopNames.length; i++) {
    buf.writeln("    BusStop(name: '${r.stopNames[i]}', distanceFromStartKm: ${r.km[i]}),");
  }
  buf.writeln('  ],');
  buf.writeln('  fareData: [');
  for (final row in fareData) {
    if (row.isEmpty) {
      buf.writeln('    [],');
    } else {

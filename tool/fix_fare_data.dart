void main() {
  // Step 1: Verify structure
  print('=== VERIFYING CORRECTED FARE DATA ===\n');
  int structErrors = 0;
  int asymmetryErrors = 0;
  int totalChecks = 0;

  for (final route in src.BusRouteData.allRoutes) {
    final correct = correctedData[route.id];
    if (correct == null) {
      print('WARNING: No corrected data for ${route.id}');
      continue;
    }

    if (correct.length != route.stops.length) {
      print('ERROR: ${route.id} has ${correct.length} fareData rows, '
          'expected ${route.stops.length}');
      structErrors++;
      continue;
    }

    for (int i = 0; i < correct.length; i++) {
      if (correct[i].length != i) {
        print('ERROR: ${route.id} fareData[$i] has ${correct[i].length} values, '
            'expected $i');
        structErrors++;
      }
    }

    // Build a temporary route with corrected data to test symmetry
    final testRoute = BusRoute(
      id: route.id,
      nameBn: route.nameBn,
      nameEn: route.nameEn,
      routeNo: route.routeNo,
      totalDistanceKm: route.totalDistanceKm,
      stops: route.stops,
      fareData: correct,
    );

    final n = route.stops.length;
    for (int i = 0; i < n; i++) {
      for (int j = 0; j < n; j++) {
        if (i == j) continue;
        totalChecks++;
        final fwd = testRoute.getFare(i, j);
        final rev = testRoute.getFare(j, i);
        if (fwd != rev) {
          print('ASYMMETRY: ${route.id} ${route.stops[i].name}($i) <-> ${route.stops[j].name}($j): $fwd vs $rev');
          asymmetryErrors++;
        }
      }
    }
  }

  print('\nStructural errors: $structErrors');
  print('Asymmetry errors: $asymmetryErrors');
  print('Symmetry checks: $totalChecks');

  if (structErrors == 0 && asymmetryErrors == 0) {
    print('\n✅ DATA IS VALID. Generating Dart file...');
    generateDartFile();
  } else {
    print('\n❌ Fix errors before generating.');
  }
}

void generateDartFile() {
  final buf = StringBuffer();
  buf.writeln("import '../models/bus_route.dart';");
  buf.writeln('');
  buf.writeln('// AUTO-GENERATED by tool/fix_fare_data.dart');
  buf.writeln('// Source: official fare chart (notification no. 35.00.0000.020.26.005.16-207)');
  buf.writeln('');
  buf.writeln('class BusRouteData {');
  buf.writeln('  static const List<BusRoute> allRoutes = [');
  
  int ri = 0;
  for (final _ in src.BusRouteData.allRoutes) {
    ri++;
    buf.writeln('    _route$ri,');
  }
  buf.writeln('  ];');
  buf.writeln('');
  buf.writeln('  static BusRoute? findById(String id) {');
  buf.writeln("    try {");
  buf.writeln("      return allRoutes.firstWhere((r) => r.id == id);");
  buf.writeln("    } catch (_) {");
  buf.writeln("      return null;");
  buf.writeln("    }");
  buf.writeln("  }");
  buf.writeln('');
  buf.writeln('  static List<BusRoute> search(String query) {');
  buf.writeln("    final q = query.toLowerCase();");
  buf.writeln("    return allRoutes.where((r) {");
  buf.writeln("      return r.nameBn.toLowerCase().contains(q) ||");
  buf.writeln("          r.nameEn.toLowerCase().contains(q) ||");
  buf.writeln("          r.routeNo.toLowerCase().contains(q) ||");
  buf.writeln("          r.stops.any((s) => s.name.toLowerCase().contains(q));");
  buf.writeln("    }).toList();");
  buf.writeln("  }");
  buf.writeln('}');
  buf.writeln('');

  ri = 0;
  for (final route in src.BusRouteData.allRoutes) {
    ri++;
    final correct = correctedData[route.id]!;
    
    buf.writeln("// Route $ri: ${route.nameBn} [${route.id}]");
    buf.writeln("const _route$ri = BusRoute(");
    buf.writeln("  id: '${route.id}',");
    buf.writeln("  nameBn: '${route.nameBn}',");
    buf.writeln("  nameEn: '${route.nameEn}',");
    buf.writeln("  routeNo: '${route.routeNo}',");
    buf.writeln("  totalDistanceKm: ${_fmtNum(route.totalDistanceKm)},");
    buf.writeln("  stops: [");
    for (final stop in route.stops) {
      buf.writeln("    BusStop(name: '${stop.name}', distanceFromStartKm: ${_fmtNum(stop.distanceFromStartKm)}),");
    }
    buf.writeln("  ],");
    buf.writeln("  fareData: [");
    for (final row in correct) {
      if (row.isEmpty) {
        buf.writeln("    [],");
      } else {
        buf.write("    [");
        buf.write(row.map((v) => _fmtNum(v)).join(', '));
        buf.writeln("],");
      }
    }
    buf.writeln("  ],");
    buf.writeln(");");
    buf.writeln('');
  }

  File('lib/data/bus_route_data.dart').writeAsStringSync(buf.toString());
  print('✅ Generated lib/data/bus_route_data.dart');
}

String _fmtNum(double v) {
  if (v == v.roundToDouble()) {
    return '${v.toInt()}.0';
  }
  return v.toString();
}

function calcFare(a, b) {
  const distance = Math.abs(a - b);
  return Math.max(MIN_FARE, Math.round(distance * FARE_RATE));
}

// Generate Python format
let pyOutput = "# ===== NEW ROUTES (A-101 to A-202) =====\n";
for (let i = 0; i < NEW_ROUTES.length; i++) {
  const r = NEW_ROUTES[i];
  const n = r.stops.length;

  // Stops
  const stopsStr = r.stops
    .map((s) => `            {"name": "${s[0]}", "distance_from_start_km": ${s[1]}}`)
    .join(",\n");

  // fare_data (triangular)
  const fareDataRows = [];
  for (let i2 = 0; i2 < n; i2++) {
    const row = [];
    for (let j = 0; j < i2; j++) {
      row.push(calcFare(r.stops[i2][1], r.stops[j][1]).toFixed(1));
    }
    fareDataRows.push(row.length > 0 ? `            [${row.join(", ")}]` : `            []`);
  }
  const fareDataStr = fareDataRows.join(",\n");

  // fare_matrix (reverse triangular)
  const fareMatrixRows = [];
  for (let i2 = 0; i2 < n - 1; i2++) {
    const row = [];
    for (let j = i2 + 1; j < n; j++) {
      row.push(calcFare(r.stops[i2][1], r.stops[j][1]).toFixed(1));
    }
    fareMatrixRows.push(`            [${row.join(", ")}]`);
  }
  const fareMatrixStr = fareMatrixRows.join(",\n");

  const entry = `    {
        "id": "${r.id}",
        "name_bn": "${r.name_bn}",
        "name_en": "${r.name_en}",
        "route_no": "${r.route_no}",
        "total_distance_km": ${r.total_distance_km},
        "stops": [
${stopsStr}
        ],
        "fare_data": [
${fareDataStr}
        ],
        "fare_matrix": [
${fareMatrixStr}
        ]
    }`;
  pyOutput += entry;
  if (i < NEW_ROUTES.length - 1) pyOutput += ",\n";
  pyOutput += "\n";
}

// Generate Dart format
let dartOutput = "";
for (let i = 0; i < NEW_ROUTES.length; i++) {
  const r = NEW_ROUTES[i];
  const varName = `_n${r.id.replace(/-/g, "_").toLowerCase()}`;
  const n = r.stops.length;

  const stopsStr = r.stops
    .map((s) => `    BusStop(name: '${s[0]}', distanceFromStartKm: ${s[1]}),`)
    .join("\n");

  const fareDataRows = [];
  for (let i2 = 0; i2 < n; i2++) {
    if (i2 === 0) {
      fareDataRows.push("    <double>[],");
    } else {
      const vals = [];
      for (let j = 0; j < i2; j++) {
        const fare = calcFare(r.stops[i2][1], r.stops[j][1]);
        vals.push(fare === Math.floor(fare) ? String(fare) : fare.toFixed(1));
      }
      fareDataRows.push(`    [${vals.join(", ")}],`);
    }
  }
  const fareDataStr = fareDataRows.join("\n");

  dartOutput += `// Route (new): ${r.name_bn} [${r.id}]\n`;
  dartOutput += `const ${varName} = BusRoute(\n`;
  dartOutput += `  id: '${r.id}',\n`;
  dartOutput += `  nameBn: '${r.name_bn}',\n`;
  dartOutput += `  nameEn: '${r.name_en}',\n`;
  dartOutput += `  routeNo: '${r.route_no}',\n`;
  dartOutput += `  totalDistanceKm: ${r.total_distance_km},\n`;
  dartOutput += `  stops: [\n`;
  dartOutput += `${stopsStr}\n`;
  dartOutput += `  ],\n`;
  dartOutput += `  fareData: [\n`;
  dartOutput += `${fareDataStr}\n`;
  dartOutput += `  ],\n`;
  dartOutput += `);\n\n`;
}

